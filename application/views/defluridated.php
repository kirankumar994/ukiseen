<h3>Problem Landscape</h3>
						<p>According to the World Health Organization (WHO) and UNICEF report, India is home to 97 million people without an access to improved water supplies. Globally there are still 780 million people without access to an improved drinking water source. In an article published by the New York Times stated that fifty percent of the water supply in rural India is usually contaminated with toxic elements. A paper released by WaterAid gives a comprehensive overview of the situation in India. It states that eighty per cent of our drinking water needs are met by groundwater, which is depleting at an alarming rate, along with large scale contamination. In the recent years there has been excessive dependence on groundwater to meet drinking water needs. If groundwater passes through fluoride rich rocks, it dissolves the fluoride and the water consequently can have more than an acceptable level of fluoride.</p>
						<p>Water scarcity itself is a big problem for people. Just getting thirsty throats quenched itself is a big task. Looking at this as a health problem, fluorosis does not yet present itself as a problem of national importance. But it definitely needs the attention of governments and local communities. According to WHO, water used for drinking should not have fluoride in excess of 1 ppm or 1.0 mg/l. In 2001, 203 districts out of 593 districts in India have shown high fluoride in groundwater as per the stats collected by the Department of Drinking Water Supply.</p>
						<p>Above 0.35 mg of fluoride per kg of body weight is considered unsafe. Using such numbers and looking at how much water people drink, an indicative number of 1 mg/l can be considered as the safety limit. However, note that for children, pregnant women and older people, the intake should be as less as possible</p>
						<p>Ingestion of low levels of fluoride compounds is beneficial to the body and prevents dental caries. Some of us can perhaps still remember advertisements of fluoridated toothpastes. But long term ingestion of excess fluoride can be harmful to the body and cause a condition known as fluorosis that affects teeth and bones.</p>
						<p>A daily intake of around 10-20 mg/day for adults and as low as 3-8 mg/day for children has been found to be harmful. Using these limits, the rough water safety limits of 1 mg/l of 1.5 mg/l have been arrived at in the context of India.</p>
						<p>There are two main types of fluorosis, namely dental and skeletal fluorosis. Dental fluorosis is caused by continuous exposures to high concentrations of fluoride during tooth development, leading to enamel with low mineral content and increased porosity. The critical period for risk to dental fluorosis is between 1 and 4 years of age. After the age of 8 when permanent teeth have established, there is lesser risk to dental fluorosis.</p>
						<p>Skeletal fluorosis is developed by the disturbance of calcium metabolism in the formation of bones of the body. It results in softening and weakening of bones resulting in deformities leading to crippling. It can also aggravate calcium related disorders such as rickets in children and osteoporosis mainly in adults. The early symptoms include stiffness and pain in the joints.  For people who are exposed to high fluoride levels for decades, severe cases of crippling can occur. In 20 states of India, more than 100 districts across the country and probably more than 60 million people are consuming drinking water which has fluoride greater than 1 mg/l. Since local food can also get irrigated by the same water, food also contains fluoride in these places. This makes the total daily consumption of fluoride more than 10 mg/day which is always harmful for adults and more so for children.</p>
						<p>There is no medicine for fluorosis, but treatment systems that can regulate the amount of fluoride in water are available. The control of drinking-water quality is therefore critical in preventing fluorosis. In all fluoride affected areas it is advised that rainwater harvesting is done to recharge the groundwater source that shows high fluoride levels.</p>
<h3>Our Vision</h3>
<p>To provide safe defluorinated water to the households for drinking and cooking with the help of awareness created by local health centres and panchayats. Since food which is irrigated by fluoride contaminated water also contains fluoride in high levels, we can plan to seek local government support to set up overhead water tank that can also defluorinate the contaminated water thus serving the purpose of reducing the fluorosis problem.</p>
